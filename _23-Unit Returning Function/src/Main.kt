fun unitFunction(name:String?): Unit{
    // BISA DIBILANG INI ADALAH VOID
    println("INI ADALAH VOID")
    println("Hi, $name")
}

fun main() {
    println("Unit Returning Function")

    // MENGGUNAKAN UNIT ITU ARTINYA ADALAH VOID PADA JAVA
    // ATAU BISA DIBILANG TIDAK MENGEMBALIKAN NILAI

    // MEMANGGIL UNIT
    unitFunction("Garaaaaaaya")
}